// :: ksg_20200527_1221 open : 화면을 따라 탭이 움직이는 스크립트(일정 높이 이상에서는 고정으로 머물도록 함.)
$(document).ready(function() {

	$(window).scroll(function() {

		// :: 탭이 올라갈 수 있는 한계 높이. (이 이상은 못 올라가고 멈춤.)
		var limitTop = $(".section.productMainInfoSect").offset().top;

		// :: 전체 페이지에서 현재 표시되는 화면이 차지하는 위치(페이지 꼭대기로부터 얼마나 먼 거리인가를 측정).
		var scrollTop = $(window).scrollTop();

		// :: 화면 스크롤 시, 탭이 위치해야 할 높이값을 계산. (현재 화면의 높이값 - 한계 고도(탭의 높이값에 한계 고도 위쪽의 불필요 공간 값이 섞여, 탭이 밑으로 가라앉는 문제를 해결하기 위해 한계 고도 값을 마이너스 처리 하는 것임.))
		var newPosition = scrollTop - limitTop + "px";

		// :: 애니메이션 들어간 졸졸이 따라다니는 스크립트. 
		if(scrollTop > limitTop) {
			$(".tabGroup.primaryTabSort").stop().animate({
				"top" : newPosition		// :: 무빙 필요 구역에서 화면을 보고 있을 때 화면의 상단에 표시되도록 함.
			}, 500);
		} if(scrollTop < limitTop) {
			$(".tabGroup.primaryTabSort").stop().animate({
				"top" : 0		// :: 무빙 외 구역(상세내용 외적 부분) 화면을 보고 있을때는 무조건 한계 높이에 존재하도록 함.
			});
		}
		
	}).scroll();
	
});
